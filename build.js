/**
 * Explyra Build Script v2.0
 * ─────────────────────────────────────────────────────────────────
 *  1. Syncs js/env.js from .env (injects Gemini key etc.)
 *  2. Copies source → www/ (excluding dev-only dirs)
 *  3. Applies mobile-specific patches to www/ files
 *  4. Reports a file+size summary
 * ─────────────────────────────────────────────────────────────────
 */

const fs   = require('fs');
const path = require('path');

const ROOT  = __dirname;
const WWW   = path.join(ROOT, 'www');
const ENVF  = path.join(ROOT, '.env');
const ENVJS = path.join(ROOT, 'js', 'env.js');

// ─────────────────────────────────────────────
//  0.  Parse .env
// ─────────────────────────────────────────────
function parseEnv(filePath) {
    if (!fs.existsSync(filePath)) return {};
    return fs.readFileSync(filePath, 'utf8')
        .split(/\r?\n/)
        .filter(l => l && !l.startsWith('#') && l.includes('='))
        .reduce((acc, l) => {
            const idx = l.indexOf('=');
            acc[l.slice(0, idx).trim()] = l.slice(idx + 1).trim();
            return acc;
        }, {});
}

// ─────────────────────────────────────────────
//  1.  Sync js/env.js from .env
// ─────────────────────────────────────────────
function syncEnvJs(env) {
    const geminiKey = env.FIREBASE_OR_GEMINI_KEY_3 || env.FIREBASE_OR_GEMINI_KEY_2 || '';
    const config = {
        firebase: {
            apiKey:            env.FIREBASE_API_KEY,
            authDomain:        env.FIREBASE_AUTH_DOMAIN,
            databaseURL:       env.FIREBASE_DATABASE_URL,
            projectId:         env.FIREBASE_PROJECT_ID,
            storageBucket:     env.FIREBASE_STORAGE_BUCKET,
            messagingSenderId: env.FIREBASE_MESSAGING_SENDER_ID,
            appId:             env.FIREBASE_APP_ID,
            measurementId:     env.FIREBASE_MEASUREMENT_ID
        },
        ai: {
            model:      'gemini-2.5-flash',
            apiKey:     'HANDLED_BY_PROXY',
            geminiKey:  geminiKey
        },
        analyticsId: env.GA_MEASUREMENT_ID,
        emailApp: {
            cloudflareZoneId: env.CLOUDFLARE_ZONE_ID,
            cloudflareToken:  'HANDLED_BY_PROXY',
            resendKey:        'HANDLED_BY_PROXY'
        },
        imagekit: {
            publicKey: env.IMAGEKIT_PUBLIC_KEY,
            url:       env.IMAGEKIT_URL
        },
        emailjs: {
            publicKey: env.EMAILJS_PUBLIC_KEY
        },
        imgbb: {
            url:    '/api/imgbb',
            apiKey: 'HANDLED_BY_PROXY'
        }
    };

    const output = `// Environment Configuration (Auto-generated by build.js — DO NOT EDIT MANUALLY)\n// Run: node build.js to regenerate.\n\nwindow.EXPLYRA_CONFIG = ${JSON.stringify(config, null, 4)};\n`;
    fs.writeFileSync(ENVJS, output, 'utf8');
    console.log('  ✓ js/env.js synced from .env');
}

// ─────────────────────────────────────────────
//  2.  Copy source → www/
// ─────────────────────────────────────────────
const IGNORE = new Set([
    'node_modules', '.git', 'android', 'www', 'functions',
    '.github', '.gemini', '.firebase', '.vscode', '.idea', '.venv',
    'dist', 'build', 'out', '__pycache__',
    'articles', // articles is a separate server app
    '.env', 'build.js', 'capacitor.config.ts'
]);

function copyDir(src, dest) {
    if (!fs.existsSync(dest)) fs.mkdirSync(dest, { recursive: true });
    for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
        if (IGNORE.has(entry.name)) continue;
        const s = path.join(src, entry.name);
        const d = path.join(dest, entry.name);
        if (entry.isDirectory()) {
            copyDir(s, d);
        } else {
            fs.copyFileSync(s, d);
        }
    }
}

function buildWww() {
    try {
        if (fs.existsSync(WWW)) {
            fs.rmSync(WWW, { recursive: true, force: true });
        }
        fs.mkdirSync(WWW, { recursive: true });
        copyDir(ROOT, WWW);
        console.log('  ✓ Source copied to www/');
    } catch (err) {
        if (err.code === 'ENOSPC') {
            console.warn('  ⚠ ENOSPC: Not enough disk space to build www/. Skipping copy.');
            console.warn('    Free up disk space and run node build.js again.');
        } else {
            throw err;
        }
    }
}

// ─────────────────────────────────────────────
//  3.  Mobile patches (applied to www/ only)
// ─────────────────────────────────────────────
function applyMobilePatches() {
    // 3a. Remove Google Sign-in containers
    const googleSigninRx = /<div id="google-signin-container"[\s\S]*?<\/div>\s*/g;
    ['admin.html', 'emp.html'].forEach(file => {
        const p = path.join(WWW, file);
        if (!fs.existsSync(p)) return;
        const before = fs.readFileSync(p, 'utf8');
        const after = before.replace(googleSigninRx, '<!-- Google Sign-in removed for mobile -->');
        if (before !== after) {
            fs.writeFileSync(p, after);
            console.log(`  ✓ Removed Google Sign-in from www/${file}`);
        }
    });

    // 3b. Redirect drive.html → external URL
    const driveRx  = /href="drive\.html"/g;
    const driveExt = 'href="https://explyra.me/drive.html"';
    ['index.html', 'emp.html', 'admin.html'].forEach(file => {
        const p = path.join(WWW, file);
        if (!fs.existsSync(p)) return;
        const before = fs.readFileSync(p, 'utf8');
        const after = before.replace(driveRx, driveExt);
        if (before !== after) {
            fs.writeFileSync(p, after);
            console.log(`  ✓ Patched Drive link in www/${file}`);
        }
    });
}

// ─────────────────────────────────────────────
//  4.  Summary
// ─────────────────────────────────────────────
function printSummary() {
    let total = 0, count = 0;
    function walk(dir) {
        for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
            const full = path.join(dir, e.name);
            if (e.isDirectory()) { walk(full); }
            else { total += fs.statSync(full).size; count++; }
        }
    }
    walk(WWW);
    const kb = (total / 1024).toFixed(1);
    console.log(`\n  📦 www/ — ${count} files, ${kb} KB total`);
}

// ─────────────────────────────────────────────
//  Run
// ─────────────────────────────────────────────
console.log('\n🔨 Explyra Build v2.0\n');

const env = parseEnv(ENVF);
syncEnvJs(env);
buildWww();
applyMobilePatches();
printSummary();

console.log('\n✅ Build complete.\n');
