import { hmacVerify, kvGet, kvPut, randomKey, jsonResponse, errorResponse } from './utils.js';

/**
 * Billing Webhook Handler
 * POST /webhook/billing
 * Body: { company_id: string, event: "plan_update", new_plan: "starter" | "growth" | "enterprise" }
 */
export async function handleBillingWebhook(request, env) {
  const signature = request.headers.get("x-webhook-signature");
  if (!signature) {
    return errorResponse("unauthorized", "Webhook signature missing", 401);
  }

  const bodyText = await request.text();
  const isValid = await hmacVerify(bodyText, signature, env.WEBHOOK_SECRET);

  if (!isValid) {
    return errorResponse("unauthorized", "Invalid webhook signature", 401);
  }

  const payload = JSON.parse(bodyText);
  const { company_id, event, new_plan } = payload;

  if (event !== "plan_update" || !new_plan) {
    return errorResponse("invalid_request", "Invalid event or plan");
  }

  const company = await kvGet(env.EXPLYRA_COMPANIES, `company:${company_id}`);
  if (!company) {
    return errorResponse("not_found", "Company not found", 404);
  }

  const oldPlan = company.plan;
  company.plan = new_plan;
  await kvPut(env.EXPLYRA_COMPANIES, `company:${company_id}`, company);

  let autoGeneratedKey = null;

  // Auto-create API key for upgrades to Growth or Enterprise
  if ((new_plan === "growth" || new_plan === "enterprise") && oldPlan === "starter") {
    autoGeneratedKey = `exp_${randomKey(24)}`;
    const keyMetadata = {
      company_id: company_id,
      label: "auto-upgrade",
      created_at: new Date().toISOString(),
      revoked: false
    };
    await kvPut(env.EXPLYRA_API_KEYS, `key:${autoGeneratedKey}`, keyMetadata);
    
    // Suggestion: Send notification email here.
    // Example: await notifyAdminEmail(company.admin_email, "Account Upgraded", "Your new API key is...");
  }

  return jsonResponse({
    message: `Plan updated to ${new_plan}`,
    auto_key: autoGeneratedKey
  });
}
